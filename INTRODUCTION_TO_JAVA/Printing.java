// Define the package for the class (optional, helps organize code)
package INTRODUCTION_TO_JAVA;

public class Printing {

    // Main method: the entry point of the program
    public static void main(String[] args){

        // Print the string "Hello" followed by a new line
        System.out.println("Hello");

        // Print the integer 3 followed by a new line
        System.out.println(3);

        // Print the floating-point number 3.41 followed by a new line
        System.out.println(3.41);

        // Print the character "A" without a new line at the end
        System.out.print("A");
    }
}

